		.section code

Command_MOSFN:	;; [mos(]
		jsr 	EXPEvalString
		jsr 	ERRCheckRParen
		lda 	zTemp0
		sta 	ControlParameters
		lda 	zTemp0+1
		sta 	ControlParameters+1
		DoSendMessage
		.byte 	1,8
		DoWaitMessage
		lda 	ControlError
		jmp 	EXPUnaryReturnA

		.send code

