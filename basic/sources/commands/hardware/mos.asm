		.section code

Command_MOS:	;; [mos]
		jsr 	EXPEvalString
		lda 	zTemp0
		sta 	ControlParameters
		lda 	zTemp0+1
		sta 	ControlParameters+1
		DoSendMessage
		.byte 	1,8
		DoWaitMessage
		lda 	ControlError
		bne 	_CMOSError
		rts
_CMOSError:
		.error_mos		



		.send code

