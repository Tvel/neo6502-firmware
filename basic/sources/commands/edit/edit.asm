		.section code

Command_Edit: ;; [edit]
		phy
		DoSendMessage 						; call initialise editor
		.byte 	13,1
		DoWaitMessage

_CELoop:		
		lda 	ControlParameters 			; was EXIT returned (code 0)
		beq 	_CEExit 					; if so, we are done.

		.byte 	3

		DoSendMessage 						; call next editor functionality
		.byte 	13,2
		DoWaitMessage
		bra 	_CELoop

_CEExit:
		ply
		rts
		
		.send code
				
